package com.prorigo.testing.demo;

public class Calculations {
	
	public static int add(int num1, int num2) {
		return num1+num2;
	}

	public static int sub(int num1, int num2) {
		return num1-num2;
	}
	
	public static boolean compareStrings(String s1, String s2) {
		return s1 == s2;
	}

}
